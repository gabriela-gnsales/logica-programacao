console.log("=== OPERADORES ===");

// Aritméticos
console.log("Aritméticos");
console.log("+ : ", "2 + 2 = ", 2 + 2); // adição
console.log("- : ", "4 - 2 = ", 4 - 2); // subtração
console.log("* : ", "2 * 3 = ", 2 * 3); // multiplicação
console.log("/ : ", "8 / 2 = ", 8 / 2); // divisão
console.log("% : ", "8 % 2 = ", 8 % 2); // módulo = resto da divisão


// Comparação
console.log("\nComparação"); // \n usa para pular linha no console
console.log("maior que:", "4 > 2 =", 4 > 2);
console.log("menor que:", "4 < 2 =", 4 < 2);
console.log("maior ou igual:", "4 >= 2 =", 4 >= 2);
console.log("menor ou igual:", "4 <= 2 =", 4 <= 2);
console.log("igualdade:", "2 == '2' =", 2 == '2'); // 1 igual só (=) é operador de ATRIBUIÇÃO e não de comparação
console.log("igualdade estrita:", "2 === '2' =", 2 === '2'); // compara não só os valores, mas tbm os tipos
console.log("diferente:", "2 != 3 =", 2 != 3);


// Lógicos
console.log("\nLógicos"); 
console.log("E (&&):", "1 > 2 && 2 > 1 =", 1 > 2 && 2 > 1); // todas as comparações precisam ser verdadeiras p/ o resultado dar verdadeiro
console.log("OU (||):", "1 > 2 || 2 > 1 =", 1 > 2 || 2 > 1); // basta que apenas 1 das operações seja verdadeira p/ o resultado dar verdadeiro
console.log("NÃO (!):", "! 1 > 2 =", ! 1 > 2); // inversão lógica
